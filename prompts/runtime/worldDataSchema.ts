- 大地点表达城市、道路、山体、水体分布；中地点表达城镇/宗门平面；小地点必须承接为中地点的一部分，建筑和道路不要脱离上层入口；具体地点若是寝居、房间、堂屋、阁楼、客房、静室等室内空间，应表达为房间、墙体、门和人物点，禁止再生成街道/野径/外部建筑。
