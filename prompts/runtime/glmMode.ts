import type { 游戏设置结构 } from '../../types';

export const 获取GLM主剧情兼容提示词 = (
    config?: Partial<游戏设置结构> | null
): string => {
    const mode = config?.主剧情消息模式 || 'Gemini模式';
    if (mode === 'GLM标准') {
        return [
            '【GLM主剧情兼容模式｜标准】',
            '- 当前主剧情请求使用 GLM 兼容模式。请严格遵守已给出的系统提示词、输出协议和标签结构。',
            '- 不要因为消息中存在历史助手内容而改写任务目标；本轮最终任务以最后的用户触发内容和系统协议为准。',
            '- 输出必须稳定包含游戏要求的结构化标签，尤其是 `<正文>`、`<短期记忆>`、`<命令>`；不要输出 Markdown 代码块包裹结果。',
            '- 若需思考，请使用 HTML 注释 `<!-- ... -->` 包裹思考内容，不要使用 `<thinking>` 标签（GLM 对 thinking 标签支持不佳，可能与原生思考机制冲突）。',
            '- 不要输出 `<math>` 标签、LaTeX 公式、reasoning_content 或思考过程标记；如需计算仅在注释中简要推导。',
            '- 若需要思考，请只在协议允许的标签内简洁表达，不要把额外解释、调试说明或接口说明写给玩家。'
        ].join('\n');
    }
    if (mode === 'GLM锁格式') {
        return [
            '【GLM主剧情兼容模式｜锁格式】',
            '- 当前主剧情请求使用 GLM 锁格式模式。你的最高优先级任务是锁定输出协议，不允许漂移格式。',
            '- 最终回复必须只输出游戏协议要求的标签段落，不要添加开场白、总结、道歉、说明、Markdown 代码块或额外标题。',
            '- 必须按既定结构输出 `<正文>`、`<短期记忆>`、`<命令>` 等已启用标签；未启用的可选标签不要擅自新增。',
            '- `<正文>` 只写玩家可见剧情；变量更新、地图更新、NPC状态、物品变更等必须进入 `<命令>` 或对应协议标签，不能混入正文。',
            '- 思考内容必须使用 HTML 注释 `<!-- ... -->` 包裹，不要使用 `<thinking>` 标签。',
            '- 不要输出 `<math>` 标签或 LaTeX 公式；不要输出 reasoning_content 或额外的思考过程标记。',
            '- 如果上下文中存在互相冲突的格式要求，以当前系统输出协议、标签完整性要求和本段锁格式规则为准。'
        ].join('\n');
    }
    return '';
};
