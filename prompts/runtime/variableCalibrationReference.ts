const 取文本 = (value: unknown): string => (typeof value === 'string' ? value : '');

const 取启用难度提示词 = (promptPool: 提示词结构[]): 提示词结构[] => (
    (Array.isArray(promptPool) ? promptPool : [])
        .filter((p) => p && p.类型 === '难度设定' && p.启用 === true)
);

const 匹配首个数值 = (text: string, patterns: RegExp[]): number | null => {
    const source = (text || '').trim();
    for (const re of patterns) {
        const m = source.match(re);
        const raw = m?.[1];
        if (!raw) continue;
        const num = Number(raw);
        if (Number.isFinite(num)) return num;
    }
    return null;
};

const 匹配首个文本 = (text: string, patterns: RegExp[]): string => {
    const source = (text || '').trim();
    for (const re of patterns) {
        const m = source.match(re);
        const raw = (m?.[1] || '').trim();
        if (raw) return raw;
    }
    return '';
};

