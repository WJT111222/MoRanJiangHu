export const 构建难度速查提示词 = (
    promptPool: 提示词结构[],
    config?: Partial<游戏设置结构> | null
): string => {
    const enabled = 取启用难度提示词(promptPool);
    if (enabled.length === 0) {
        return '【变量相关提示词｜难度速查】\n- 当前未启用任何难度设定提示词。';
    }

    const gamePrompt = enabled.find((p) => typeof p?.id === 'string' && p.id.startsWith('diff_game_'));
    const physPrompt = enabled.find((p) => typeof p?.id === 'string' && p.id.startsWith('diff_phys_'));

    const lines: string[] = ['【变量相关提示词｜难度速查】'];

    if (gamePrompt) {
        const g = 提炼游戏难度倍率(gamePrompt, config);
        lines.push(`- 游戏难度：${g.档位}`);
        lines.push(`  - 敌方倍率：生命 x${g.敌方生命 ?? '？'} / 攻击 x${g.敌方攻击 ?? '？'} / 防御 x${g.敌方防御 ?? '？'}`);
        lines.push(`  - 资源经济：经验 x${g.经验 ?? '？'} / 掉落 x${g.掉落 ?? '？'} / 任务 x${g.任务 ?? '？'} / 物价 x${g.物价 ?? '？'}`);
        lines.push(`  - 生存压力：精力消耗 x${g.精力消耗 ?? '？'} / 饱腹口渴消耗 x${g.饱腹口渴消耗 ?? '？'}`);
        const 游戏难度修炼补充 = 取修炼附加文本([
            `  - 生存压力补充：内力消耗 x${g.内力消耗 ?? '？'}`,
            `  - 恢复倍率补充：内力恢复 x${g.内力恢复 ?? '？'}`
        ].join('\n'), config);
        lines.push(`  - 恢复倍率：安全区 x${g.安全区恢复 ?? '？'} / 非安全区 x${g.非安全区恢复 ?? '？'}`);
        if (游戏难度修炼补充) lines.push(游戏难度修炼补充);
    }

    if (physPrompt) {
        const p = 提炼生理难度阈值(physPrompt, config);
        lines.push(`- 生理难度：${p.档位}`);
        lines.push(`  - 精力：行动消耗 x${p.行动消耗倍率 ?? '？'}；自然恢复 x${p.精力自然恢复倍率 ?? '？'}；轻疲劳<=${p.轻疲劳阈值 ?? '？'}% / 重疲劳<=${p.重疲劳阈值 ?? '？'}%`);
        const 生理难度修炼补充 = 取修炼附加文本(
            `  - 内力：功法消耗 x${p.功法消耗倍率 ?? '？'}；自然恢复 x${p.内力自然恢复倍率 ?? '？'}；低内力<=${p.低内力阈值 ?? '？'}% / 枯竭<=${p.枯竭阈值 ?? '？'}%`,
            config
        );
        if (生理难度修炼补充) lines.push(生理难度修炼补充);
        lines.push(`  - 饱腹/口渴：每小时 饱腹-${p.饱腹每小时 || '？'}；口渴-${p.口渴每小时 || '？'}；归零扣血 ${p.归零扣血 || '？'}`);
    }

    return lines.join('\n').trim();
};

