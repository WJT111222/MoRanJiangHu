import { describe, expect, it } from 'vitest';
import { 解析当前有效性别比例, 地点级比例有效 } from '../utils/genderRatioUtils';
import type { 环境信息结构 } from '../models/environment';
import type { 地图层级结构 } from '../models/world';

describe('解析当前有效性别比例', () => {
    const mockEnv: 环境信息结构 = {
        时间: '1:01:01:00:00',
        季节: '春季',
        天气: '晴',
        大地点: '大陆A',
        中地点: '区域B',
        小地点: '城市C',
        区地点: '',
        子地点: '',
        具体地点: ''
    } as 环境信息结构;

    it('地点级比例优先于世界级比例', () => {
        const 地图层级列表: 地图层级结构[] = [
            {
                ID: 'DT-001',
                名称: '城市C',
                层级: '小地点',
                父级ID: 'DT-000',
                描述: '',
                性别比例: { 男: 10, 女: 90, 男娘: 0, 扶她: 0 },
                性别比例恢复回合: 5,
                性别比例变更原因: '测试事件'
            }
        ];

        const result = 解析当前有效性别比例(
            mockEnv,
            地图层级列表,
            { 男: 50, 女: 50, 男娘: 0, 扶她: 0 }, // 世界级
            { 男: 60, 女: 40, 男娘: 0, 扶她: 0 }  // 开局配置
        );

        expect(result).toEqual({ 男: 10, 女: 90, 男娘: 0, 扶她: 0 });
    });

    it('小地点优先于中地点', () => {
        const 地图层级列表: 地图层级结构[] = [
            {
                ID: 'DT-001',
                名称: '区域B',
                层级: '中地点',
                父级ID: 'DT-000',
                描述: '',
                性别比例: { 男: 30, 女: 70, 男娘: 0, 扶她: 0 },
                性别比例恢复回合: -1
            },
            {
                ID: 'DT-002',
                名称: '城市C',
                层级: '小地点',
                父级ID: 'DT-001',
                描述: '',
                性别比例: { 男: 10, 女: 90, 男娘: 0, 扶她: 0 },
                性别比例恢复回合: 5
            }
        ];

        const result = 解析当前有效性别比例(
            mockEnv,
            地图层级列表,
            undefined,
            { 男: 60, 女: 40, 男娘: 0, 扶她: 0 }
        );

        expect(result).toEqual({ 男: 10, 女: 90, 男娘: 0, 扶她: 0 });
    });

    it('恢复回合为0的地点级比例视为无效', () => {
        const 地图层级列表: 地图层级结构[] = [
            {
                ID: 'DT-001',
                名称: '城市C',
                层级: '小地点',
                父级ID: 'DT-000',
                描述: '',
                性别比例: { 男: 10, 女: 90, 男娘: 0, 扶她: 0 },
                性别比例恢复回合: 0, // 异常状态
                性别比例变更原因: '已过期'
            }
        ];

        const result = 解析当前有效性别比例(
            mockEnv,
            地图层级列表,
            { 男: 50, 女: 50, 男娘: 0, 扶她: 0 },
            undefined
        );

        // 应 fallback 到世界级
        expect(result).toEqual({ 男: 50, 女: 50, 男娘: 0, 扶她: 0 });
    });

    it('世界级比例优先于开局配置', () => {
        const result = 解析当前有效性别比例(
            mockEnv,
            [],
            { 男: 40, 女: 60, 男娘: 0, 扶她: 0 },
            { 男: 60, 女: 40, 男娘: 0, 扶她: 0 }
        );

        expect(result).toEqual({ 男: 40, 女: 60, 男娘: 0, 扶她: 0 });
    });

    it('全部未设置时返回开局配置', () => {
        const result = 解析当前有效性别比例(
            mockEnv,
            [],
            undefined,
            { 男: 60, 女: 40, 男娘: 0, 扶她: 0 }
        );

        expect(result).toEqual({ 男: 60, 女: 40, 男娘: 0, 扶她: 0 });
    });

    it('全部未设置时返回 undefined', () => {
        const result = 解析当前有效性别比例(mockEnv, [], undefined, undefined);
        expect(result).toBeUndefined();
    });

    it('支持字符串格式性别比例', () => {
        const 地图层级列表: 地图层级结构[] = [
            {
                ID: 'DT-001',
                名称: '城市C',
                层级: '小地点',
                父级ID: 'DT-000',
                描述: '',
                性别比例: '男:女',
                性别比例恢复回合: 5
            }
        ];

        const result = 解析当前有效性别比例(mockEnv, 地图层级列表, undefined, undefined);
        expect(result).toBe('男:女');
    });
});

describe('地点级比例有效', () => {
    it('恢复回合 > 0 视为有效', () => {
        const node: 地图层级结构 = {
            ID: 'DT-001',
            名称: '城市C',
            层级: '小地点',
            父级ID: 'DT-000',
            描述: '',
            性别比例: { 男: 10, 女: 90, 男娘: 0, 扶她: 0 },
            性别比例恢复回合: 5
        };

        expect(地点级比例有效(node)).toBe(true);
    });

    it('恢复回合 = -1（永久性）视为有效', () => {
        const node: 地图层级结构 = {
            ID: 'DT-001',
            名称: '女性村落',
            层级: '小地点',
            父级ID: 'DT-000',
            描述: '',
            性别比例: { 男: 0, 女: 100, 男娘: 0, 扶她: 0 },
            性别比例恢复回合: -1
        };

        expect(地点级比例有效(node)).toBe(true);
    });

    it('恢复回合 = 0（异常状态）视为无效', () => {
        const node: 地图层级结构 = {
            ID: 'DT-001',
            名称: '城市C',
            层级: '小地点',
            父级ID: 'DT-000',
            描述: '',
            性别比例: { 男: 10, 女: 90, 男娘: 0, 扶她: 0 },
            性别比例恢复回合: 0
        };

        expect(地点级比例有效(node)).toBe(false);
    });

    it('性别比例字段不存在视为无效', () => {
        const node: 地图层级结构 = {
            ID: 'DT-001',
            名称: '城市C',
            层级: '小地点',
            父级ID: 'DT-000',
            描述: '',
            性别比例恢复回合: 5
        };

        expect(地点级比例有效(node)).toBe(false);
    });

    it('恢复回合未设置但有性别比例字段视为无效', () => {
        const node: 地图层级结构 = {
            ID: 'DT-001',
            名称: '城市C',
            层级: '小地点',
            父级ID: 'DT-000',
            描述: '',
            性别比例: { 男: 10, 女: 90, 男娘: 0, 扶她: 0 }
        };

        expect(地点级比例有效(node)).toBe(false);
    });
});
